<?php
    function sum($t1, $t2){
        $sum = $t1 + $t2;
        return $sum;
    }
    
    function multiply($t1, $t2){
        $prod = $t1 * $t2;
        return $prod;
    }

    function sub($t1, $t2){
        $diff = $t1 - $t2;
        return $diff;
    }
    function div($t1, $t2){
        $quot = $t1/$t2;
        return $quot;
    }
?>